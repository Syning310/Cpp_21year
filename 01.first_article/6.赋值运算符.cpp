#include <iostream>
using namespace std;

int main() {
	int a = 1;

	//И│охн╦╦сич
	// +=
	a = 10;
	a += 2; //a = a + 2
	cout << "a = " << a << endl;

	// -=
	a = 10;
	a -= 2;
	cout << "a = " << a << endl;

	// *= 
	a = 10;
	a *= 2;
	cout << "a = " << a << endl;

	// /=
	a = 10;
	a /= 2;
	cout << "a = " << a << endl;

	// %=
	a = 10;
	a %= 2;
	cout << "a = " << a << endl;

	system("pause");
	return 0;
}