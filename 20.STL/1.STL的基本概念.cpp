

//STL（Standard Template Library，标准模板库）
//SLT从广义上分为：容器（container）、算法（algorithm）、迭代器（iterator） ；三个铁哥们，结拜
//容器 和 算法 之间通过 迭代器 进行无缝连接    //算法要通过迭代器才可以访问容器中的元素
//STL几乎所有的代码都采用了模板类或模板函数

//STL大体分为六大组件：分别是：容器、算法、迭代器、仿函数、适配器（配接器）、空间配置器
//容器：各种数据结构，如vectoe、list、deque、set、map等，用来存放数据
//算法：各种常用的算法，如sort(排序)、find（查找）、copy（拷贝）、for_each（遍历）等
//迭代器：扮演了容器与算法之间的胶合剂     //迭代器可当作指针来看
//仿函数：行为类似函数，可作为算法的某种策略
//适配器：一种用来修饰容器或者仿函数或迭代器接口的东西
//空间配置器：负责空间的配置与管理

